#include <iostream> //find area of circle MAKETEST
using namespace std;

int main()
{
    float PI = 3.14159;
    float r = 4;
    float radius;
    float area;
    
    cout << "Finding area of circle" << endl;
    cout << "Value of PI = " << PI << endl;
    
    radius = r*r;
    area = radius*PI;
    
    cout << "Area of circle = " << area << endl;
    
    
    
}